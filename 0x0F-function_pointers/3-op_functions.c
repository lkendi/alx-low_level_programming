#include "3-calc.h"
/**
 * op_add - adds2 numbers
 * @a: first number
 * @b: second number
 * Return: sum of the numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 numbers
 * @a: first number
 * @b: second number
 * Return: difference of the 2 numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 numbers
 * @a: first number
 * @b: second number
 * Return: product of the 2 numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 numbers
 * @a: first number
 * @b: second number
 * Return: divison result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the division remainder
 * @a: first number
 * @b: second number
 * Return: division remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
